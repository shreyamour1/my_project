package com.radiusAgent.demo.model;

public class ParameterPercentage {

	private Property property;

	private Double distance;

	private Double budget;

	private Double bedRoom;

	private Double bathRoom;

	private Double total;

	public Property getProperty() {
		return property;
	}

	public void setProperty(Property property) {
		this.property = property;
	}

	public Double getDistance() {
		return distance;
	}

	public void setDistance(Double distance) {
		this.distance = distance;
	}

	public Double getBudget() {
		return budget;
	}

	public void setBudget(Double budget) {
		this.budget = budget;
	}

	public Double getBedRoom() {
		return bedRoom;
	}

	public void setBedRoom(Double bedRoom) {
		this.bedRoom = bedRoom;
	}

	public Double getBathRoom() {
		return bathRoom;
	}

	public void setBathRoom(Double bathRoom) {
		this.bathRoom = bathRoom;
	}

	public Double getTotal() {
		return total;
	}

	public void setTotal(Double total) {
		this.total = total;
	}
}
